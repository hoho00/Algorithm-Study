int main()
{
	int N, M;
	scanf("%d %d", &N, &M);
	for (int i = 0; i < N; i++) {
		for (int j = 0; j < M; j++) {
			scanf("%d", &a[i][j]);
		}
	}
	printf("\n");
	int sum = 0;
	for (int i = 0; i < N; i++) {
		for (int j = 0; j < M; j++) {
			for (int k = 0; k < 27; k++) {
				int tmpSum = 0;
				for (int l = 0; l < 4; l++) {
					if((i + tetro_y[k][l]) < 0 || (j + tetro_x[k][l]) < 0 || (i + tetro_y[k][l]) >= N || (j + tetro_x[k][l]) >= M) {
						tmpSum = 0;
						break;
					}
					else {
						tmpSum += a[i + tetro_y[k][l]][j + tetro_x[k][l]];
					}
				}
				if (tmpSum > sum) {
					sum = tmpSum;
					printf("%d %d %d %d\n", i, j, k, sum);
				}
			}
		}
	}
	
	printf("%d", sum);
	
	
	return 0;
}
